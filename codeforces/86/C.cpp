#include <bits/stdc++.h>

using namespace std;

void solve() {
    int a, b, q;
    std::cin >> a >> b >> q;
    if (a < b) swap(a, b);
    int minus = a;
    int k = __gcd(a, b);
    int cyc = a * b / k;
    std::vector<bool> v(cyc, true);
    for (size_t i = 0; i < a; i++) v[i] = false;
    for (int i = 0; i < q; i++) {
        int l, r, c = 0;
        std::cin >> l >> r;
        // if (a%b==0) std::cout << 0 << '\n';
        // else {
            int ll = l % cyc;
            int rr = r % cyc;
            for (size_t j = ll; j < cyc; j++) {
                if (v[j]) c++;
            }
            for (size_t j = 0; j < rr; j++) {
                if (v[j]) c++;
            }
            std::cout << c + (r-l)/cyc * (cyc-a) << '\n';
        // }
    }
}

int main(int argc, char const *argv[]) {
    int t;
    std::cin >> t;
    while (t--) solve();
}
